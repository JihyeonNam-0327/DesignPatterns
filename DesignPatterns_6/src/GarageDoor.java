
public class GarageDoor {

	void up() {
		System.out.println("차고 문이 열립니다~");
	}
	
	void down() {
		System.out.println("차고 문이 닫힙니다~");
	}
	
	void stop() {
		System.out.println("차고 문의 작동을 정지시킵니다.");
	}
	
	void lightOn() {
		System.out.println("차고의 불을 켭니다.");
	}
	
	void lightOff() {
		System.out.println("차고의 불을 끕니다.");
	}
}
